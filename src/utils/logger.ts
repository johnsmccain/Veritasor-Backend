/**
 * Simple logger utility. Can be expanded or replaced with a more
 * sophisticated logging library in the future.
 */

export const logger = {
  info: (...args: any[]) => console.log('[INFO]', ...args),
  warn: (...args: any[]) => console.warn('[WARN]', ...args),
  error: (...args: any[]) => console.error('[ERROR]', ...args),
};
